/*
 * Each file you create when creating an app with ember-cli
 * becomes an "ES6 Module".  A module is a small part of a
 * program with it's own scope.
 *
 * Nothing in the module is exposed to other modules or the
 * application unless it directly uses the global scope or
 * "exports" it.
 *
 * Code created with modules is "modular". If you think about
 * IKEA furniture or similar "modular" things you have used
 * or interacted with in your life, a module is a single, standalone
 * item.  "imports" are other items (modules) it is connected to.
 *
 * "exports" are points on the item where other things can connect.
 * "exports" are the "public interface" or "programming interface" ("api")
 * for the module.
 *
 * You can "export" any named variable or function using
 * "export".
 *
 * In this exercise, you need to export three functions (named `foo`, `bar` and `baz`)
 * as well as export a default function `spam` which returns `true` when invoked.
 *
 * You can read about functions here:
 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Guide/Functions
 *
 * If you were googling, don't read this unless you want, it comes later:
 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Function
 *
 * You can read about exports here:
 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/export
 *
 */

export function foo() {}

export function bar() {}

export function baz() {}

export default function spam() {}
